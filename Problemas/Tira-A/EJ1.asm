.EQU SUM = 0x120    ; DEFINO UN ESPACIO EN MEMORIA PARA CARGAR LOS INSCIRPTOS
MOV R16, R1  ; MUEVO LOS INSCRIPTOS DEL CURSO 1 AL REGISTRO R16 DONDE HAOG LA SUMA
LDI R17, 20  ; LIMITE DE INSCRIPTOS
SUB R17, R1  ; A R17 LE RESTO LOS INSCIRPTOS
BREQ sobreinscriptos
LDI R17, 20
SUB R17, R2
BREQ sobreinscriptos
LDI R17, 20
SUB R17, R3
BREQ sobreinscriptos
LDI R17, 20
SUB R17, R4
BREQ sobreinscriptos
ADD R16, R2  ; SUMO INSCRIPTOS DEL CURSO 2
ADD R16, R3  ; SUMO INSCRIPTOS DEL CURSO 3
ADD R16, R4  ; SUMO INSCRIPTOS DEL CURSO 4
STS SUM, R16 ;   MUEVO LA SUMA A LA MEMORIA
sobreinscriptos:
    RET

